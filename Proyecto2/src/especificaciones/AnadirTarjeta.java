    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        ListaDobleCircular lis = new ListaDobleCircular();
        Columna buscar = lis.Buscar(Integer.parseInt(jLabel6.getText()));
        if (buscar!= null) {
            String tipo = buscar.getTipo();
           
            if (tipo.equalsIgnoreCase("Pila")) {
                lis.agregarpila(Integer.parseInt(jLabel6.getText()), jTextField1.getText(), jTextArea1.getText());
                JOptionPane.showMessageDialog(null, "Tarjeta Guardada Correctamente");
                lis.mostrar(Integer.parseInt(jLabel6.getText()));
                 Tableros nf = new Tableros(jLabel5.getText());
       nf.setVisible(true);
       dispose();
                
            }else if (tipo.equalsIgnoreCase("Cola")) {
                lis.agregarcola(Integer.parseInt(jLabel6.getText()), jTextField1.getText(), jTextArea1.getText());
                 Tableros nf = new Tableros(jLabel5.getText());
       nf.setVisible(true);
       dispose();
            }else if (tipo.equalsIgnoreCase("Lista Doblemente Enlazada")) {
                lis.agregarListaDoble(Integer.parseInt(jLabel6.getText()), jTextField1.getText(), jTextArea1.getText());
                 Tableros nf = new Tableros(jLabel5.getText());
       nf.setVisible(true);
       dispose();
            }
            else if (tipo.equalsIgnoreCase("Doble Circular")) {
                lis.agregarListaDoble(Integer.parseInt(jLabel6.getText()), jTextField1.getText(), jTextArea1.getText());
                 Tableros nf = new Tableros(jLabel5.getText());
       nf.setVisible(true);
       dispose();
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

